<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Jenssegers\Mongodb\Eloquent\Model as Eloquent;

use App\Models\Customer;

// class CustomersAddress extends Model
class CustomersAddress extends Eloquent
{
    use HasFactory;

    protected $connection = 'mongodb';
	protected $collection = 'customers_address';
    protected $primaryKey = '_id';

    /**
     * The name of the "created at" column.
     *
     * @var string
     */
    const CREATED_AT = 'createdAt';

    /**
     * The name of the "updated at" column.
     *
     * @var string
     */
    const UPDATED_AT = 'updatedAt';

    /**
     * Get the customer that owns the CustomersAddress
     *
     * @return \Illuminate\Database\Eloquent\Relations\BelongsTo
     */
    public function customer()
    {
        return $this->belongsTo(Customer::class, 'customer_id', '_id');
    }

}
